          :user="authUser"
          :class="{ 'is-hidden': !(isSelected === 'records') }"
        />
      </div>
      <div class="right-container dashboard-sub-container">
        <UserCalendar
          :sports="sports"
          :user="authUser"
          :class="{ 'is-hidden': !(isSelected === 'calendar') }"
        />
        <Timeline
          :sports="sports"
          :authUser="authUser"
          :class="{ 'is-hidden': !(isSelected === 'timeline') }"
        />
      </div>
    </div>
    <div id="bottom" />
  </div>
  <div v-else class="app-loading">
    <Loader />
  </div>
